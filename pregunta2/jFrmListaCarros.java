    public void mostrarCarrosEnTabla() {
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();

        model.setRowCount(0);

        for (Carro carro : listaCarros) {
            model.addRow(new Object[]{null, carro.getModelo(), carro.getColor(),
                null, carro.getModalidadSeguro(), carro.getAnioFabricacion(),
                carro.getTipoCarro(), carro.isPinturaMetalizada()});
        }
    }

